package JavaBasics;

public class Typecastingdemo {

	public static void main(String[] args) {
		double d = 15/0.5d;
		int i = (int)d;
		System.out.println(d);
		System.out.println(i);
		
		float f = 121/3f;
		double my = (double)f;
		System.out.println(my);
		System.out.println(f);
		
		
		

	}

}
